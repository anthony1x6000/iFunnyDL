package com.anth1x.ifunnydl;

public class UrlTime {
    private String url;
    private String time;

    public UrlTime(String url, String time) {
        this.url = url;
        this.time = time;
    }

    public String getUrl() {
        return url;
    }

    public String getTime() {
        return time;
    }
}